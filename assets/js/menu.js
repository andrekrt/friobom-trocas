
function menuSetores(){

    if(document.getElementById("submenuSetor").style.display == "none" || document.getElementById("submenuSetor").style.display == "" ){
        document.getElementById("submenuSetor").style.display = "block";
    }else{
        document.getElementById("submenuSetor").style.display = "none";
    }
    
}

